#include <stdio.h>
#include <stdlib.h>

/**
 * add - adds two integers
 * @a: first int
 * @b: second int
 * Return: addition
 */

int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts two ints
 * @a: first int
 * @b: second int
 * Return: sub
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two integers
 * @a: 1st integer
 * @b: 2nd int
 * Return: mul
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - mod between two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: mod
 */
int mod(int a, int b)
{
	return (a % b);
}

